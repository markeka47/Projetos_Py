nome = "Guilherme Arthur de Carvalho"

#Fatiamento de String
# Exibe os caracteres conforme a iteração da string

# Exibe o caractere "inicial" - G  
print(nome[0])

# Exibe o caractere -2 do final da string
print(nome[-2])

# Exibe os caracteres do "0" até o "9"
print(nome[:9])

#Exibe a partir do caractere "01" 
print(nome[10:])

# Exibe o intervalo de caracteres do "10" ao "16"
print(nome[10:16])

# Exibe o intervalo de caracteres do "10" ao "16" e o "02" a partir do "16"
print(nome[10:16:2])

# Exibe a string completa 
print(nome[:])

# Exibe a string espelhada 
print(nome[::-1])
