# conversão de float para inteiro
print(int(1.5))

# Conversão de inteiro para float
print(float(1))

# Conversão de string para inteiro
print(int('10'))

# Conversão de string para float
print(float('10.0'))

# Conversão em float
print(float(100))

# float (uma barra converte automaticamente float)
print(100 / 2)

# inteiro (duas barras converte em inteiro)
print(100 // 2)